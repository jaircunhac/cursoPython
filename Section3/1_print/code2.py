# sep= means a separator, it changes the way it shows the separation.

# end= means what it's going to be the breakLine, is it's going to be "\n", "\r\n" or otherThings("$$", "&&", "@@" etc.)

print(12, 34, "cinco e seis", sep="_", end="\n")
print(78, 910, "onze e doze", sep="", end="$$")
print(1314, 1516, "dezesete e dezoito", end="\r\n")